    static final String[] SINGLE_VOICE_PATCH_NUMBERS = {
            "01", "02", "03", "04", "05", "06", "07", "08", "09", "10", "11",
            "12", "13", "14", "15", "16" };
    static final String[] SINGLE_VOICE_BANK_NUMBERS = {
            "Internal:A", "Internal:B", "Internal:C", "Internal:D", "Card:A",
            "Card:B", "Card:C", "Card:D", "Preset1:A", "Preset1:B",
            "Preset1:C", "Preset1:D", "Preset2:A", "Preset2:B", "Preset2:C",
            "Preset2:D" };

    static final String SYSEXID = "F043**7A****4C4D2020383130315643";

    static final int DEV_ID_OFS = 2;

    public YamahaSY77VoiceDriver() {
        super("Voice", YamahaSY77Device.author);
        sysexID = SYSEXID;
        patchNameStart = 33;
        patchNameSize = 10;
        deviceIDoffset = DEV_ID_OFS;
        numSysexMsgs = 1;
        sysexRequestDump =
                new SysexHandler(
                        "F0 43 @@ 7A 4C 4D 20 20 38 31 30 31 56 43 "
                                + "00 00 00 00 00 00 00 00 00 00 00 00 00 00 *bankNum* *patchNum* F7");
        bankNumbers = SINGLE_VOICE_BANK_NUMBERS;
        patchNumbers = SINGLE_VOICE_PATCH_NUMBERS;
    }

    public void storePatch(Patch p, int bankNum, int patchNum) {
        // change the address to internal voice
        p.sysex[30] = (byte) 0;
        p.sysex[31] = (byte) ((bankNum & 4) * 16 + patchNum);
        calculateChecksum(p);
        sendPatchWorker(p);
    }

    public void sendPatch(Patch p) {
        // change the address to internal voice
        p.sysex[30] = (byte) 0x7f;
        p.sysex[31] = (byte) 0;
        calculateChecksum(p);
        sendPatchWorker(p);
    }

    public Patch createNewPatch() {
        Patch p = new Patch(initVoiceSysex, this);
        setPatchName(p, "INIT VOICE");
        calculateChecksum(p);
        return p;
    }

    public void requestPatchDump(int bankNum, int patchNum) {
        send(sysexRequestDump.toSysexMessage(getChannel() + 0x20,
                new SysexHandler.NameValue("bankNum", bankNum >> 2),
                new SysexHandler.NameValue("patchNum", (bankNum & 3) * 16
                        + patchNum)));
    }

    public void calculateChecksum(Patch p) {
        YamahaSY77Sysex.checksum(((Patch) p).sysex);
    }

