package synthdrivers.AlesisQS;

import static org.junit.Assert.assertEquals;

import org.junit.Test;

public class SysexRoutinesTest {

    /**
     * Test it out on a real Sysex message that I captured - this is a dump of
     * Mix 90, which has "Jazz Piano" in position 0, and nothing much in any of
     * the other positions.
     */
    private static final byte[] JAZZ_PIANO = {
            (byte) 0xf0, 0x00, 0x00, 0x0e, 0x0e, 0x0e, 0x5a, 0x41, 0x2a, 0x50,
            0x56, 0x16, 0x00, 0x2c, 0x32, 0x50, 0x73, 0x13, 0x00, 0x00, 0x47,
            0x3f, 0x32, 0x0a, 0x03, 0x78, 0x7f, 0x07, 0x10, 0x58, 0x7d, 0x64,
            0x14, 0x46, 0x77, 0x7f, 0x0f, 0x20, 0x20, 0x7c, 0x49, 0x29, 0x0c,
            0x60, 0x2e, 0x1f, 0x00, 0x40, 0x75, 0x13, 0x53, 0x18, 0x40, 0x7f,
            0x3e, 0x00, 0x60, 0x78, 0x27, 0x26, 0x31, 0x00, 0x7f, 0x79, 0x00,
            0x40, 0x71, 0x4f, 0x4c, 0x62, 0x00, 0x7e, 0x73, 0x01, 0x00, 0x63,
            0x1f, 0x19, 0x45, 0x01, 0x7c, 0x67, 0x03, 0x00, 0x46, 0x3f, 0x32,
            0x0a, 0x03, 0x78, 0x4f, 0x07, 0x00, 0x0c, 0x7f, 0x64, 0x14, 0x06,
            0x70, 0x1f, 0x0f, 0x00, 0x18, 0x7e, 0x49, 0x29, 0x0c, 0x60, 0x3f,
            0x1e, 0x00, 0x30, 0x7c, 0x13, 0x53, 0x18, 0x40, 0x7f, 0x3c, 0x00,
            0x60, 0x78, 0x27, 0x26, 0x31, 0x00, 0x7f, 0x79, 0x00, 0x40, 0x71,
            0x4f, 0x4c, 0x62, 0x00, 0x7e, 0x73, 0x01, 0x00, 0x63, 0x1f, 0x19,
            0x45, 0x01, 0x7c, 0x67, 0x03, 0x00, 0x46, 0x3f, 0x32, 0x0a, 0x03,
            0x78, 0x4f, 0x07, 0x00, 0x0c, 0x7f, 0x64, 0x14, 0x06, 0x70, 0x1f,
            0x0f, (byte) 0xf7 };

    @Test
    public void testGetBits() {
        byte[] bytes = {
                0, 0, 0, 112, 14 };
        assertEquals("Test1", 27, SysexRoutines.getBits(bytes, 0, 30, 5, false));
        assertEquals("Test2", -5, SysexRoutines.getBits(bytes, 0, 30, 5, true));
        assertEquals("Effect Channel", 0, SysexRoutines.getBits(JAZZ_PIANO,
                QSConstants.HEADER, (0 << 3) + 4, 4, false));
    }

    @Test
    public void testGetChars() {
        String patchName =
                SysexRoutines
                        .getChars(JAZZ_PIANO, QSConstants.HEADER,
                                QSConstants.MIX_NAME_START,
                                QSConstants.MIX_NAME_LENGTH);
        assertEquals("PatchName", "Jazz Piano", patchName);

        SysexRoutines.setChars("ZellynHunter", JAZZ_PIANO, QSConstants.HEADER,
                QSConstants.MIX_NAME_START, QSConstants.MIX_NAME_LENGTH);
        patchName =
                SysexRoutines
                        .getChars(JAZZ_PIANO, QSConstants.HEADER,
                                QSConstants.MIX_NAME_START,
                                QSConstants.MIX_NAME_LENGTH);
        assertEquals("PatchName Modified", "ZellynHunt", patchName);

    }
}
